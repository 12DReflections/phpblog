            <p>The concept of 'ontological formations' refers to formations of social relations understood as 
	    dominant ways of living. Temporal, spatial, corporeal, epistemological and performative relations are 
	    taken to be central to understanding a dominant formation. That is, a particular ontological formation 
	    is based on how ontological categories of time, space, embodiment, knowing and performing are lived—objectively 
	    and subjectively. Different ontological formations include the customary (including the tribal), the traditional,
	    the modern and the postmodern. The concept was first introduced by Paul James' Globalism, Nationalism
	     Tribalism together with a series of writers including Damian Grenfell and Manfred Steger.
	     --Wikipedia, 'ontology'</p>
