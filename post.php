	     The processes by which bodies related to environments became of great concern, and the idea of being
	     itself became difficult to really define. What did people mean when they said "A is B", "A must be B",
	     "A was B"...? Some linguists advocated dropping the verb "to be" from the English language, leaving 
	     "E Prime", supposedly less prone to bad abstractions. Others, mostly philosophers, tried to dig into t
	     he word and its usage. Heidegger distinguished human being as existence from the being of things in
	     the world. Heidegger proposes that our way of being human and the way the world is for us are cast 
	     historically through a fundamental ontological questioning. These fundamental ontological categories
	     provide the basis for communication in an age: a horizon of unspoken and seemingly unquestionable
	     background meanings, such as human beings understood unquestioningly as subjects and other 
	     entities understood unquestioningly as objects. Because these basic ontological meanings both 
	     generate and are regenerated in everyday interactions, the locus of our way of being in a historical 
	     epoch is the communicative event of language in use.[12] For Heidegger, however, communication 
	     in the first place is not among human beings, but language itself shapes up in response to questioning
	     (the inexhaustible meaning of) being.[15] Even the focus of traditional ontology on the 'whatness' or
	     'quidditas' of beings in their substantial, standing presence can be shifted to pose the question of the
	     'wholeness' of human being itself.[16]
